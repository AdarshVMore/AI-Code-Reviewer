import {
  getOctokit,
  getDifferenceData,
  getReviewRules,
} from "./github.service.js";
import { getAIReview, reviewPrompt, parseAIResponse } from "./ai.service.js";
import { db } from "../../../package/db/prisma.js";

function formatReviewComment(parsed: any): string {
  const score = parsed.score ?? "N/A";
  const issues: any[] = parsed.issues ?? [];

  const highCount = issues.filter((i) => i.severity === "high").length;
  const mediumCount = issues.filter((i) => i.severity === "medium").length;
  const lowCount = issues.filter((i) => i.severity === "low").length;

  let md = `## ReviewPilot — AI Code Review\n\n`;
  md += `> ${parsed.summary}\n\n`;
  md += `**Score:** ${score}/100`;
  if (issues.length > 0) {
    md += ` · ${highCount} high · ${mediumCount} medium · ${lowCount} low`;
  }
  md += `\n\n---\n\n`;

  if (issues.length === 0) {
    md += `No issues found.\n`;
    return md;
  }

  md += `### Issues\n\n`;
  for (const issue of issues) {
    md += `#### \`${issue.file}:${issue.line}\`\n\n`;
    md += `| Field | Value |\n|---|---|\n`;
    md += `| **Severity** | ${issue.severity} |\n`;
    md += `| **Category** | ${issue.category} |\n\n`;
    md += `**Problem**\n\n${issue.problem}\n\n`;
    md += `**Fix**\n\n${issue.fix}\n\n`;
    md += `---\n\n`;
  }

  md += `<sub>Generated by ReviewPilot</sub>\n`;
  return md;
}

export type PRReviewJobData = {
  installationId: number;
  owner: string;
  repo: string;
  prNumber: number;
  prTitle?: string;
};

export async function runPRReview(data: PRReviewJobData) {
  const { installationId, owner, repo, prNumber, prTitle } = data;

  const octokit = await getOctokit(installationId);
  const difference = await getDifferenceData(octokit, owner, repo, prNumber);
  const { data: collaborators } = await octokit.rest.repos.listCollaborators({
    owner,
    repo,
  });
  const rules = await getReviewRules(octokit, owner, repo, prNumber);

  const prompt = reviewPrompt(difference, rules);
  const aiResponse = await getAIReview(prompt);
  const cleanText = aiResponse
    .replace(/```json/g, "")
    .replace(/```/g, "")
    .trim();
  const parsedPrompt = parseAIResponse(cleanText);

  if (!parsedPrompt) {
    console.log("❌ Failed to parse AI response");
    return;
  }

  const commentBody = formatReviewComment(parsedPrompt);
  await octokit.issues.createComment({
    owner,
    repo,
    issue_number: prNumber,
    body: commentBody,
  });

  const repository = await db.repository.findUnique({
    where: { owner_name: { owner, name: repo } },
  });

  if (!repository) {
    console.log(
      `repo ${owner}/${repo} not found in DB after upsert — skipping save`,
    );
    return aiResponse;
  }

  const savedReview = await db.pRReview.create({
    data: {
      prNumber,
      prTitle: prTitle ?? null,
      summary: parsedPrompt.summary ?? null,
      score: parsedPrompt.score ?? 0,
      repositoryId: repository.id,
      author: owner,
      commitSHA: "",
    },
  });

  if (parsedPrompt.issues?.length > 0) {
    await db.issue.createMany({
      data: parsedPrompt.issues.map((issue: any) => ({
        reviewId: savedReview.id,
        file: issue.file,
        line: issue.line,
        category: issue.category,
        severity: issue.severity,
        problem: issue.problem,
        fix: issue.fix,
      })),
    });
  }

  console.log(`review saved to DB for ${owner}/${repo}#${prNumber}`);
  return aiResponse;
}
